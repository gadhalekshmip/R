# Define a variable

a <- as.integer(readline("Enter value a:"))
b <- as.integer(readline("Enter value b:"))


# Example 1: if statement
if (a > b) {
  print("a is greater than b")
}

# Example 2: if-else statement
if (a > b) {
  print("a is greater than b")
} else {
  print("b is greater than a")
}
